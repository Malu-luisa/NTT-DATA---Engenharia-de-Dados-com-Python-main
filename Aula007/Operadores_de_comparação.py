# Comparadores
saldo = 200  # Definindo a variável 'saldo' com o valor 200
saque = 200  # Definindo a variável 'saque' com o valor 200

# Igualdade
print(saldo == saque)  # Verifica se 'saldo' é igual a 'saque'
# >>> True (200 é igual a 200)

# Diferença
print(saldo != saque)  # Verifica se 'saldo' é diferente de 'saque'
# >>> False (200 não é diferente de 200)

# Menor que
print(saldo < saque)  # Verifica se 'saldo' é menor que 'saque'
# >>> False (200 não é menor que 200)

# Menor ou igual que
print(saldo <= saque)  # Verifica se 'saldo' é menor ou igual a 'saque'
# >>> True (200 é igual a 200)

# Maior que
print(saldo > saque)  # Verifica se 'saldo' é maior que 'saque'
# >>> False (200 não é maior que 200)

# Maior ou igual que
print(saldo >= saque)  # Verifica se 'saldo' é maior ou igual a 'saque'
# >>> True (200 é igual a 200)
