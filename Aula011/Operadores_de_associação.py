# Operadores de pertencimento
# São utilizados para verificar se um objeto está presente em uma sequência (como listas, strings, etc.)

frutas = ['limão', 'uva']  # Definindo uma lista de frutas

# Verifica se 'laranja' está presente na lista 'frutas'
print('laranja' in frutas)  # False: 'laranja' não está na lista 'frutas'

# Verifica se 'uva' está presente na lista 'frutas'
print('uva' in frutas)  # True: 'uva' está na lista 'frutas'

# Verifica se 'limão' não está presente na lista 'frutas'
print('limão' not in frutas)  # False: 'limão' está na lista 'frutas'
