# Inteiros
A = 11  # Definindo a variável 'A' como um número inteiro
B = 10  # Definindo a variável 'B' como um número inteiro
print(A+B)  # Soma os valores das variáveis 'A' e 'B'
# >>> 21
print(type(A), type(B))  # Exibe o tipo de dado de 'A' e 'B', que são inteiros (int)
# >>> <class 'int'> <class 'int'>

# Flutuante (números com ponto decimal)
A = 11.00  # Definindo a variável 'A' como um número flutuante (decimal)
B = 10.00  # Definindo a variável 'B' como um número flutuante (decimal)
print(A+B)  # Soma os valores das variáveis 'A' e 'B'
# >>> 21.0
print(type(A), type(B))  # Exibe o tipo de dado de 'A' e 'B', que são flutuantes (float)
# >>> <class 'float'> <class 'float'>

# Booleano (valores verdadeiros ou falsos)
A = True  # Definindo a variável 'A' como um valor booleano True
B = False  # Definindo a variável 'B' como um valor booleano False
print(A)  # Exibe o valor de 'A', que é True
# >>> True
print(B)  # Exibe o valor de 'B', que é False
# >>> False
print(type(A), type(B))  # Exibe o tipo de dado de 'A' e 'B', que são booleanos (bool)
# >>> <class 'bool'> <class 'bool'>

# String (sequência de caracteres)
A = 'Maria'  # Definindo a variável 'A' como uma string
B = ' Luisa'  # Definindo a variável 'B' como uma string (incluindo um espaço no início)
print(A+B)  # Concatena as strings 'A' e 'B'
# >>> 'Maria Luisa'
print(type(A), type(B))  # Exibe o tipo de dado de 'A' e 'B', que são strings (str)
# >>> <class 'str'> <class 'str'>
