# Input
nome = input('Informe seu nome: ')  # A função input() recebe uma entrada do usuário
print(nome)  # Exibe o valor inserido pelo usuário
# Exemplo: Se o usuário inserir "Maria", o output será "Maria"

# Print
nome = 'Maria'  # Definindo a variável 'nome' com o valor 'Maria'
sobrenome = 'Luisa'  # Definindo a variável 'sobrenome' com o valor 'Luisa'

# A função print() exibe os valores de 'nome' e 'sobrenome' com um espaço entre eles por padrão
print(nome, sobrenome)
# >>> Maria Luisa
