# Operações de atribuição

saldo = 500  # Atribui o valor 500 à variável 'saldo'
print(saldo)
# >>> 500

saldo = 200  # Redefine o valor de 'saldo' para 200
print(saldo)
# >>> 200

# Atribuição com adição
saldo += 10  # Equivalente a: saldo = saldo + 10 (adiciona 10 ao saldo atual)
print(saldo)
# >>> 210

# Atribuição com subtração
saldo -= 5  # Equivalente a: saldo = saldo - 5 (subtrai 5 do saldo atual)
print(saldo)
# >>> 205

# Atribuição com divisão
saldo /= 2  # Equivalente a: saldo = saldo / 2 (divide o saldo por 2)
print(saldo)
# >>> 102.5

# Atribuição com multiplicação
saldo *= 10  # Equivalente a: saldo = saldo * 10 (multiplica o saldo por 10)
print(saldo)
# >>> 1025.0

# Atribuição com divisão inteira
saldo //= 2  # Equivalente a: saldo = saldo // 2 (divide o saldo por 2 e remove a parte decimal)
print(saldo)
# >>> 512.0

# Atribuição com módulo
saldo %= 3  # Equivalente a: saldo = saldo % 3 (calcula o resto da divisão do saldo por 3)
print(saldo)
# >>> 2.0

# Atribuição com exponenciação
saldo **= 2  # Equivalente a: saldo = saldo ** 2 (eleva o saldo à potência de 2)
print(saldo)
# >>> 4.0
